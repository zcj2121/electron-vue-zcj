/* 验证 密码 数字、大小写字母*/
export function validatePwd(str) {
  var reg = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)[^]{6,12}$/
  return reg.test(str)
}

/* 6位 验证码*/
export function validateSixCode(str) {
  var reg = /^\d{6}$/
  return reg.test(str)
}

